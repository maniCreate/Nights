    override func tableView(_ tableView: UITableView, willDisplayHeaderView view: UIView, forSection section: Int) {
        guard let header = view as? UITableViewHeaderFooterView else { return }
        
        let bottomBorder = CALayer()
        bottomBorder.frame = CGRect(x: 0, y: header.contentView.frame.maxY, width: header.contentView.frame.width, height: 0.7)
        bottomBorder.backgroundColor = UIColor.lightGray.cgColor
        
        header.layer.addSublayer(bottomBorder)
        
        //Headerのラベルの文字色を設定
        header.textLabel?.textColor = UIColor.lightGray
        //Headerの背景色を設定
        header.contentView.backgroundColor = UIColor.rgba(red: 51, green: 51, blue: 51, alpha: 1)
    }
    
